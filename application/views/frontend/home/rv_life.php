        <h3 class="sub--title">RV Life</h3>
          <p class="style9">WHY Go RVing?</p>
          <p><br>
            <span class="style10">That Perfect Place. Before Everyone Else Does. </span><em><strong><font size="2"><br>
            </font></strong></em><br>
            <br>
            <img src="<?=base_url();?>/assets/img/whygorving_pic.jpg" alt="RV Life Living" width="260" hspace="7" height="176" align="left">The freedom to pursue your passions. The flexibility 
              to pursue them when you want, where you want, how 
              you want. The fun of a vacation experience unlike 
              any other. <br>
              <br>
              Freedom, flexibility and fun: That’s the RV 
                difference.<br>
              <br>
              There’s taking a trip from Point A to Point B. And 
                then there’s RVing. Recreation vehicles give you 
                more control, convenience and comfort than other 
                forms of travel. With an RV, you can hit the road 
                more often, for longer periods, for less money. <br>
              <br>
              The reasons you go RVing are the same reasons you 
              take any vacation. To get a break from the daily 
              routine. To be with family and friends. To rest. To 
              relax. To see new places.<br>
              <br>
              But the difference between RVing and other types of 
              vacations is that RVing allows you to truly achieve 
              all those goals — and more. <br>
              <br>
              Another difference? With RVing, there are no flights 
              to catch. No security hassles. No long lines or lost 
              luggage. No hauling heavy bags in and out of costly 
              hotel rooms. No expensive unhealthy food. <br>
              <br>
              RV owners surveyed by Harris Interactive cited 
              escaping the routine of the everyday world, enjoying 
              nature and resting, and relaxing and recharging 
              their batteries as the main reasons for taking RV 
              trips. <br>
              <br>
              <br>
              <br>
              <em><strong>The Road is Calling …</strong></em><br>
              <br>
              The back yard has a fence. The playground has a 
              fence. The ball field has a fence. <br>
              <br>
              <br>
              <img src="<?=base_url();?>/assets/img/hittheroad_pic2.jpg" width="270" hspace="7" height="183" align="left">Beyond 
               the fences, new worlds are waiting.<br>
             <br>
             A few generations ago, most Americans took only one 
             weeklong vacation a year. Today, people yearn to get 
             away on shorter trips more often — and at all times 
               of the year. <br>
             <br>
             An RV makes that possible, allowing you to strike 
             out on the road whenever you want and go nearly 
             anywhere you want. With so many destinations, events 
             and adventures — and models ranging from van campers 
               to fifth-wheel travel trailers — from which to 
               choose, RV travelers are always on the move, making 
               memories every mile of the way.<br>
             <br>
                 “...the reason people hit the road in RVs is the 
               same today as it ever was: People love their 
               freedom.” <br>
